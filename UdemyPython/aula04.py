# Tipo int e float
# int -> Numero inteiro
# O tipo int representa qualquer numero positivo ou negativo. 
# int sem sinal é considerado positivo.

print(11) #int
print(-11) #int
print(0) #int

# float -> Numero com ponto flutuante, com casa decimal
# O tipo float representa qualquer numero positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo

print(1.30) #float
print(2.0, -10.5) #float

# A função type mostra o tipo que o Python mostre ao valor
print(type(10))
print(type(1.4))
print(type('Vinicius'))
